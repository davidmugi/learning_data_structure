package org.example.FastSlowPointer.Easy;

public class ListNode {

    int val = 0;
    ListNode next;

    public ListNode(int value) {
        this.val = value;
    }

    public int getVal() {
        return val;
    }

    public void setVal(int val) {
        this.val = val;
    }

    public ListNode getNext() {
        return next;
    }

    public void setNext(ListNode next) {
        this.next = next;
    }
}
